#include <stdio.h>


// Scrieţi un program care să verifice dacă 3 numere întregi date a, b, c pot forma lungimile laturilor unui triunghi.


int main()
{
	int a,b,c;
	scanf("%d %d %d" , &a, &b, &c);
	if (a + b >= c && b + c >= a && a + c >= b && a >= 0 && b >= 0 && c >= 0) {
		printf("DA");
	}
	else printf("NU");
return 0;
}
